						from the dust of the ground and breathed his Spirit into him to bring him to life. From
						this man God made a woman so that the woman was both distinct from the man and one with
						him in substance and in spirit (Genesis 2:7, 21-23). Adam and Eve were truly “One Flesh”
						made to complement each other in order to fulfill the first command of God (Genesis
						1:28) to join with their Creator and continue creation through the conception and
						nurturing of children. The child conceived in the womb possesses that same sacred life
