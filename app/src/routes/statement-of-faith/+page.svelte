						We believe that Jesus Christ, as our representative and substitute (I Peter 3:18), shed
						His blood on the cross as the perfect, all-sufficient sacrifice for our sins (I Peter
						1:18-19; Hebrews 10:10, 14, 17). His atoning death and victorious resurrection
						constitute the only ground for salvation (Ephesians 2:8-9). In an age when many profess
						that there are many paths to God, we affirm that genuine faith in Jesus Christ alone is
						the only means of salvation and eternal life (Acts 4:12; John 14:6).
