						Romans 6:3-5), which visibly and tangibly express the gospel (I Corinthians 11:23-26).
						Though they are not the means of salvation (Ephesians 2:8-9; Galatians 5:1), when
						celebrated by the church in genuine faith, these ordinances confirm and nourish the
						believer (Romans 6:3-6; I Corinthians 11:26, 28). As the Father disciplines the ones he
