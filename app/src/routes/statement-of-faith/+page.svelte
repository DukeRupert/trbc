						Corinthians 5:1-13). In the ministry of the church, both men and women are encouraged to
						serve Christ and to be developed to their full potential in the manifold ministries of
						the people of God (I Corinthians 12:12-14; 18-25). The distinctive leadership role
						within the church – the Family of God – given to men of mature Christian character and
						experience (I Timothy 3:1-2; 4-5) is grounded in Creation, the Fall, and Christ’s work
						of redemption and must not be sidelined by appeals to cultural developments (I Timothy
